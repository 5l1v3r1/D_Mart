CREATE TABLE MALL (
    City VARCHAR(10),
    Dong VARCHAR(10),
    Roadname_address VARCHAR(20),
    Detailed_address VARCHAR(20),
    Mnumber INT not null,
    PRIMARY KEY (Mnumber)
    );

CREATE TABLE CATEGORY(
    Large_category VARCHAR(15),
    Small_category VARCHAR(15),
    Cnumber INT not null,
    PRIMARY KEY(Cnumber)
    );
   
CREATE TABLE PRODUCER_AND_LOCATION(
    Producer_ID INT not null,
    PRIMARY KEY(Producer_ID)
    );


 CREATE TABLE ITEM(
    IDate DATE,
    Stock INT,
    IName VARCHAR(20),
    Mnum INT,
    INumber INT not null,
    Importer VARCHAR(20),
    Producer VARCHAR(20),
    Origin VARCHAR(20),
    P_ID INT,
    Cnum INT,
    FOREIGN KEY(Mnum) REFERENCES MALL(Mnumber),
    FOREIGN KEY(P_ID) REFERENCES PRODUCER_AND_LOCATION(Producer_ID),
    FOREIGN KEY(Cnum) REFERENCES CATEGORY(Cnumber),
    PRIMARY KEY(INumber)
    );

 CREATE TABLE CUSTOMER(
    CustomerID INT not null,
    Password INT not null,
    Age INT,
    Gender VARCHAR(5),
    Phone_number VARCHAR(15),
    Job VARCHAR(10),
    Roadname_address VARCHAR(20),
    Detailed_address VARCHAR(20),
    PRIMARY KEY(CustomerID)
    );

CREATE TABLE CART (
 CDate date ,
 CartID int NOT NULL,
 CustomerIDe int NOT NULL,
 PRIMARY KEY (CartID,CustomerIDe),
 FOREIGN KEY (CustomerIDe) REFERENCES CUSTOMER (CustomerID)
); 

CREATE TABLE CART_PRODUCT_LIST(
    Product_list VARCHAR(20) not null,
    CartIDe INT not null,
    CustomerIDen INT not null,
    Inum_c INT not null,
    PRIMARY KEY(Product_list,CartIDe,CustomerIDen,Inum_c),
    FOREIGN KEY(CartIDe) REFERENCES CART(CartID),
    FOREIGN KEY(CustomerIDen) REFERENCES CART(CustomerIDe)
    );

 CREATE TABLE IN_A_CART(
    Inum int not null,
    CartIDen int not null,
    CustomerIDent int not null,
    PRIMARY KEY(Inum,CartIDen,CustomerIDent),
    FOREIGN KEY(Inum) REFERENCES ITEM(Inumber),
    FOREIGN KEY(CartIDen) REFERENCES CART(CartID),
    FOREIGN KEY(CustomerIDent) REFERENCES CART(CustomerIDe)
    );


  CREATE TABLE ORDER_(
    Mno INT not null,
    CustomerIDenti INT not null,
    Start_time DATETIME,
    End_time DATETIME,
    Onumber INT not null,
    UNIQUE KEY(Onumber),
    PRIMARY KEY(Mno,CustomerIDenti,Onumber),
    FOREIGN KEY (Mno) REFERENCES MALL(Mnumber),
    FOREIGN KEY (CustomerIDenti) REFERENCES CUSTOMER(CustomerID)
    );

  CREATE TABLE ORDER_LIST(
    lMno INT not null,
    lCustomerIDenti INT not null,
    lOnumber INT not null,
    Order_list VARCHAR(50) not null,
    PRIMARY KEY(lMno,lCustomerIDenti,lOnumber,Order_list),
    FOREIGN KEY(lMno) REFERENCES ORDER_(Mno),
    FOREIGN KEY(lCustomerIDenti) REFERENCES ORDER_(CustomerIDenti),
    FOREIGN KEY(lOnumber) REFERENCES ORDER_(Onumber)
    );


